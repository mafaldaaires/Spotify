CREATE DATABASE IF NOT EXISTS `SPOTIFY`;
USE `SPOTIFY`;

DROP TABLE IF EXISTS FEATURES, PLAYLIST, USER, 
HAS, SONG, RELEASES, STYLE, ARTIST, GENRE, ALBUM;
    
CREATE TABLE IF NOT EXISTS SONG(
	IdSong INTEGER PRIMARY KEY,
    Composer VARCHAR(100) NOT NULL,
    Duration INT(6) NOT NULL,
    Title VARCHAR(100) NOT NULL);
    
CREATE TABLE IF NOT EXISTS ARTIST(
	IdArtist INTEGER PRIMARY KEY AUTO_INCREMENT,
    ArtistName VARCHAR(100) NOT NULL,
    `ArtisticalName ?` VARCHAR(100),
    Country VARCHAR (32) NOT NULL);
    
CREATE TABLE IF NOT EXISTS STYLE(
	IdArtist INT(11) NOT NULL,
    Styles VARCHAR (64) NOT NULL,
    PRIMARY KEY (`IdArtist`, Styles),
    KEY Styles (Styles),
	CONSTRAINT `artist_styles_ibfk_1` FOREIGN KEY (`IdArtist`) REFERENCES `ARTIST` (`IdArtist`) ON UPDATE CASCADE);
    
CREATE TABLE IF NOT EXISTS ALBUM(
	IdAlbum INTEGER PRIMARY KEY NOT NULL,
    Title VARCHAR (100) NOT NULL,
    Year SMALLINT(6) NOT NULL);
    
CREATE TABLE IF NOT EXISTS USER(
	Username VARCHAR(100) PRIMARY KEY NOT NULL,
    Followers INT NOT NULL,
    Following INT NOT NULL);
    
CREATE TABLE IF NOT EXISTS PLAYLIST(
	IdPlaylist INTEGER PRIMARY KEY,
    Creator VARCHAR(100) NOT NULL,
    `Name` VARCHAR(100) NOT NULL,
    TotalDuration INT(10) NOT NULL,
    FOREIGN KEY (Creator) REFERENCES USER(Username));
    
CREATE TABLE IF NOT EXISTS FEATURES(
	Playlist INT(100) NOT NULL,
    Song INT(100) NOT NULL,
    PRIMARY KEY (Playlist, Song),
    FOREIGN KEY (Playlist) REFERENCES PLAYLIST(IdPlaylist),
    FOREIGN KEY (Song) REFERENCES SONG(IdSong));
    
CREATE TABLE IF NOT EXISTS HAS(
	Album INT(100) NOT NULL,
    Song INT(100) NOT NULL,
    PRIMARY KEY(Album, Song),
    FOREIGN KEY (Album) REFERENCES ALBUM(IdAlbum),
    FOREIGN KEY (Song) REFERENCES SONG(IdSong));
    
CREATE TABLE IF NOT EXISTS RELEASES (
	Artist INT(100) NOT NULL,
    Album INT(100) NOT NULL,
    PRIMARY KEY(Artist, Album),
    FOREIGN KEY (Artist) REFERENCES ARTIST(IdArtist),
    FOREIGN KEY (Album) REFERENCES ALBUM(IdAlbum));
    
CREATE TABLE IF NOT EXISTS GENRE (
	Album INTEGER NOT NULL,
    GenreName VARCHAR(32),
    PRIMARY KEY (Album, GenreName),
    FOREIGN KEY (Album) REFERENCES ALBUM(IdAlbum));
    
    
